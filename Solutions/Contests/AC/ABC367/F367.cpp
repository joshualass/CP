template<typename T>
struct Tree {
    static constexpr T base = {1,1,1,1,1};
    vector<T> v;
    int n, size;
    T f(T a, T b) { //change this when doing maximum vs minimum etc.
        T res;
        for(int i = 0; i < 5; i++) res[i] = a[i] * b[i];
        return res;
    }
    Tree(int n, T def = base) {
        this->n = n; //max number of elements
        size = 1;
        while(size < n) size *= 2;
        v.assign(size * 2, def);
    }
    void update(int pos, T val) { //update 0 indexed, assignment
        assert(pos < n && pos >= 0);
        int curr = pos + size;
        v[curr] = val;
        while(curr != 1) {
            if(curr & 1) { //handles non-communative operations
                v[curr / 2] = f(v[curr ^ 1], v[curr]);
            } else {
                v[curr / 2] = f(v[curr], v[curr ^ 1]);
            }
            curr /= 2;
        }
    }
    T at(int idx) {
        assert(idx >= 0 && idx < n);
        return v[idx + size];
    }
    T query(int l, int r) {//queries in range [l,r)
        return _query(1,0,size,l,r);
    }
    T _query(int idx, int currl, int currr, int &targetl, int &targetr) {
        if(currr <= targetl || currl >= targetr) return base;
        if(currl >= targetl && currr <= targetr) return v[idx];
        int mid = (currl + currr) / 2;
        return f(
            _query(idx * 2, currl, mid, targetl, targetr),
            _query(idx * 2 + 1, mid, currr, targetl, targetr)
        );
    }
};

mt19937_64 rng(std::chrono::steady_clock::now().time_since_epoch().count());

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, q; cin >> n >> q;
    array<Z,5> r;
    for(int i = 0; i < 5; i++) r[i] = rng() % P;

    Tree<array<Z,5>> a(n), b(n);
    for(int i = 0; i < n; i++) {
        array<Z,5> nx;
        ll x; cin >> x;
        for(int j = 0; j < 5; j++) nx[j] = x + r[j];
        a.update(i, nx);
    }

    for(int i = 0; i < n; i++) {
        array<Z,5> nx;
        ll x; cin >> x;
        for(int j = 0; j < 5; j++) nx[j] = x + r[j];
        b.update(i, nx);
    }

    for(int i = 0; i < q; i++) {
        int l, r, L, R; cin >> l >> r >> L >> R;
        l--; L--;
        cout << (a.query(l, r) == b.query(L, R) ? "Yes" : "No") << "\n";
    }

    return 0;
}
