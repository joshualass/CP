void solve() {
    int n; cin >> n;
    vector<int> a(n), b(n);
    for(int &x : a) cin >> x;
    for(int &x : b) cin >> x;

    Z res = 2;
    for(int i = 1; i < n; i++) {
        if(max(a[i-1], b[i-1]) <= min(a[i], b[i])) {
            res *= 2;
        }
    }
    cout << res << '\n';
}

