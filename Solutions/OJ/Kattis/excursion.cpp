typedef long long ll;
typedef long double ld;
using namespace std;

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    string s; cin >> s;
    ll res = 0;
    ll o = 0, t = 0;
    for(auto c : s) {
        if(c == '0') {
            res += o + t;
        }
        if(c == '1') {
            res += t;
            o++;
        }
        if(c == '2') {
            t++;
        }
    }
    cout << res << '\n';
    return 0;
}
