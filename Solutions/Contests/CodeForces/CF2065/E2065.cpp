void solve() {
    
    int n, m, k; cin >> n >> m >> k;

    if(n < k && m < k || abs(n-m) > k) {
        cout << "-1\n";
        return;
    }

    string res = "";
    int cur = -1;
    if(n > m) {
        cur = 0;
    } else {
        cur = 1;
    }
    
    while(n || m) {
        if(cur == 0) {
            res.append(string(min(n,k), '0'));
            n -= min(n, k);
        } else {
            res.append(string(min(m,k), '1'));
            m -= min(m, k);
        }
        cur ^= 1;
    }
    cout << res << '\n';
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int casi; cin >> casi;
    while(casi-->0) solve();

    return 0;
}