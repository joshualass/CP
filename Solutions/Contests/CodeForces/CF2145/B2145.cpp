void solve() {
    
    int n, k; cin >> n >> k;
    string s; cin >> s;
    int tc = count(s.begin(), s.end(), '0');
    int bc = count(s.begin(), s.end(), '1');
    int oc = count(s.begin(), s.end(), '2');

    for(int i = 0; i < n; i++) {
        //check if it has to be removed
        if(i < tc || i >= n - bc || n == k) {
            cout << '-';
        } else if(i < tc + oc || i >= n - (bc + oc)) {
            cout << '?';
        } else {
            cout << '+';
        }
    }
    cout << '\n';
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int casi; cin >> casi;
    while(casi-->0) solve();

    return 0;
}