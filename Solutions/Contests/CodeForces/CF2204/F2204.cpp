signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m; cin >> n >> m;
    vector<array<int,2>> a(n);
    for(int i = 0; i < n; i++) {
        cin >> a[i][0];
        a[i][1] = i;
    }
    sort(a.begin(), a.end());
    set<int> s;
    s.insert(-1);
    s.insert(n);

    vector<Z> ics(n);
    Z always = 0;
    Z weighted_n = 0;

    for(auto [val, i] : a) {
        Z ic = Z(i - (*--s.lower_bound(i))) * Z((*s.lower_bound(i)) - i);
        ics[i] = ic;
        weighted_n += Z(ic) / val;
        Z tot = Z(i - (-1)) * Z(n - i);
        always += (tot - ic) / val;
        // cout << "val : " << val << " i : " << i << " ic : " << ic << " tot : " << tot << endl;
        s.insert(i);
    }

    // cout << "always : " << always << endl;
    // cout << "wn : " << weighted_n << endl;
    // cout << "ics : " << ics << endl;

    int p = 0;
    Z setup = 0, ready = 0;

    for(int qq = 0; qq < m; qq++) {
        int k; cin >> k;
        while(p < n && k + 1 >= a[p][0]) {
            auto [val, i] = a[p++];
            weighted_n -= ics[i] / val;
            setup += ics[i] * (val - 1);
            ready += ics[i];
            // cout << "vis p : " << p - 1 << " val : " << val << " i : " << i << endl;
        }

        // cout << "qq : " << qq << " wn : " << weighted_n << " setup : " << setup << " ready : " << ready << endl;

        cout << always + weighted_n * (k + 1) + (ready * (k + 1) - setup) << '\n';
    }   

    return 0;
}
