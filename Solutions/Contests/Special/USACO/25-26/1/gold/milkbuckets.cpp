template<typename T>
struct Tree {
    static constexpr T base = 0;
    vector<T> v;
    int n, size;
    T f(T a, T b) { //change this when doing maximum vs minimum etc.
        return a + b;
    }
    Tree(int n, T def = base) {
        this->n = n; //max number of elements
        size = 1;
        while(size < n) size *= 2;
        v.assign(size * 2, def);
    }
    void update(int pos, T val) { //update 0 indexed, assignment
        assert(pos < n && pos >= 0);
        int curr = pos + size;
        v[curr] = val;
        while(curr != 1) {
            if(curr & 1) { //handles non-communative operations
                v[curr / 2] = f(v[curr ^ 1], v[curr]);
            } else {
                v[curr / 2] = f(v[curr], v[curr ^ 1]);
            }
            curr /= 2;
        }
    }
    T at(int idx) {
        assert(idx >= 0 && idx < n);
        return v[idx + size];
    }
    T query(int l, int r) {//queries in range [l,r)
        return _query(1,0,size,l,r);
    }
    T _query(int idx, int currl, int currr, int &targetl, int &targetr) {
        if(currr <= targetl || currl >= targetr) return base;
        if(currl >= targetl && currr <= targetr) return v[idx];
        int mid = (currl + currr) / 2;
        return f(
            _query(idx * 2, currl, mid, targetl, targetr),
            _query(idx * 2 + 1, mid, currr, targetl, targetr)
        );
    }
};

void solve() {
    
    int n; cin >> n;
    map<int,vector<int>> m;
    for(int i = 0; i < n; i++) {
        int x; cin >> x;
        m[x].push_back(i);
    }

    ll cost = 0;
    Tree<int> tree(n, 0);

    for(auto it = m.rbegin(); it != m.rend(); it++) {
        for(int x : it->second) tree.update(x, 1);
        for(int x : it->second) {
            ll add = min(x - tree.query(0, x), (n - (x + 1)) - tree.query(x + 1, n));
            
            // cout << "level : " << it->first << " x : " << x << " add : " << add << '\n';
            ll lhs = tree.query(0, x);
            ll rhs = tree.query(x + 1, n);

            // cout << "lhs : " << lhs << " rhs : " << rhs << '\n';

            cost += add;
        }
    }

    cout << cost << '\n';

}

