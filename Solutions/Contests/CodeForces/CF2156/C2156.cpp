void solve() {
    
    int n, k; cin >> n >> k;
    vector<int> a(n);
    for(int &x : a) cin >> x;
    sort(a.begin(), a.end());
    int res = 1;
    map<int,int> m;
    for(int x : a) m[x]++;
    for(int g = 2; g <= n; g++) {
        int good = m[g] + m[g * 2] + m[g * 3] + (a.end() - lower_bound(a.begin(), a.end(), g * 4));
        if(n - good <= k) {
            res = g;
        }
    }   
    cout << res << '\n';
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int casi; cin >> casi;
    while(casi-->0) solve();

    return 0;
}