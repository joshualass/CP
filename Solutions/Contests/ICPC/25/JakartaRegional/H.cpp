Z solve(ll m, ll d) {
    ll n = d / 2;
    Z M = Z(m);
    Z Mp1 = Z(m + 1);
    Z N = Z(n);

    if (d == 1) {
        return Z(2);
    }

    Z ans = 0;

    if (d % 2 == 0) {
        ans = power(Mp1, n) + N + Z(1);

        // ans += (m * n - 1) * ( (m+1)^(n-1) - 1 ) * inv(m)
        ans += (M * N - Z(1)) * (power(Mp1, n - 1) - Z(1)) / M;

        // ans -= m * ( (m+1)^n * (n-1) - (m+1)^(n-1) * n + m + 1 ) * inv(m^2)
        Z tmp = power(Mp1, n) * Z(n - 1)
              - power(Mp1, n - 1) * N
              + M + Z(1);
        ans -= M * tmp / (M * M);

        return ans;
    } else {
        ans = power(Mp1, n) * Z(2)
            - power(Mp1, n - 1)
            + N + Z(1);

        ans += (M * N - Z(1)) * (power(Mp1, n - 1) - Z(1)) / M;

        Z tmp = power(Mp1, n) * Z(n - 1)
              - power(Mp1, n - 1) * N
              + M + Z(1);
        ans -= M * tmp / (M * M);

        return ans;
    }
}

