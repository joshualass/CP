typedef long long ll;
typedef long double ld;
using namespace std;

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll n; cin >> n;
    vector<ll> a(n);
    for(ll &x : a) cin >> x;

    ll res = 0;
    sort(a.begin(), a.end());
    for(int i = 0; i < n; i++) {
        for(int j = i + 1; j < n; j++) {
            for(int k = j + 1; k < n; k++) {
                if(a[i] + a[j] > a[k]) res += 1LL << ((k - j) - 1);
            }
        }
    }


    cout << res << '\n';

    return 0;
}
