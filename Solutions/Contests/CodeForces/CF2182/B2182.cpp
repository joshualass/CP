typedef long long ll;
typedef long double ld;
using namespace std;

void solve() {
    
    ll a, b; cin >> a >> b;
    
    auto _solve = [](ll f, ll s) -> ll {
        int res = 0;
        while(1) {
            if(res % 2 == 0) {
                if(f < (1 << res)) break;
                f -= 1 << res;
            } else {
                if(s < (1 << res)) break;
                s -= 1 << res;
            }
            res++;
        }
        return res;
    };

    cout << max(_solve(a,b), _solve(b,a)) << '\n';

}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int casi; cin >> casi;
    while(casi-->0) solve();

    return 0;
}