void solve() {
    
    int n; cin >> n;
    vector<int> a(1 << n);
    iota(a.begin(), a.end(), 0);

    sort(a.begin(), a.end(), [&](const auto &lhs, const auto &rhs) -> bool {
        int lscore = -1, rscore = -1;
        while((lhs >> (lscore + 1)) & 1) lscore++;
        while((rhs >> (rscore + 1)) & 1) rscore++;
        if(lscore != rscore) return lscore > rscore;
        return lhs < rhs;
    });

    for(int i = 0; i < a.size(); i++) {
        cout << a[i] << " \n"[i == a.size() - 1];
    }

}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int casi; cin >> casi;
    while(casi-->0) solve();

    return 0;
}