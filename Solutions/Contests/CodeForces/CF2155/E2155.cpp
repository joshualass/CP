void solve() {
    
    int n, m, k; cin >> n >> m >> k;
    vector<int> p(m);
    for(int i = 0; i < k; i++) {
        int x, y; cin >> x >> y;
        p[y - 1] ^= 1;
    }

    int ok;

    if(n == 1) {
        if(m >= 2) {
            ok = p[1];
        } else {
            ok = 0;
        }
    } else {
        ok = 0;
        for(int i = 1; i < m; i++) ok |= p[i];
    }

    cout << (ok ? "Mimo" : "Yuyu") << '\n';

}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int casi; cin >> casi;
    while(casi-->0) solve();

    return 0;
}