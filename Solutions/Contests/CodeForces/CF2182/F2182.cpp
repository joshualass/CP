int cnts[61];

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    init_fact();

    int n, m; cin >> n >> m;

    for(int i = 0; i < n; i++) {
        int x; cin >> x;
        cnts[x]++;
    }

    for(int qq = 0; qq < m; qq++) {
        ll type, x; cin >> type >> x;
        if(type == 1) {
            cnts[x]++;
        } else if(type == 2) {
            cnts[x]--;
        } else {
            Z res = 0;
            Z mul = 1;
            for(int i = 60; i >= 0; i--) {
                //no good, x is too large!
                if((1LL << (i + 1)) <= x) {
                    mul = 0;
                    break;
                }

                // cout << "i : " << i << " x : " << x << '\n';

                int need = 0;
                while((1LL << i) <= x) {
                    x -= 1LL << i;
                    x <<= 1;
                    need++;
                }

                if(need > cnts[i]) {
                    mul = 0;
                    break;
                }

                int rem = 0;
                for(int k = 0; k < i; k++) rem += cnts[k];

                Z ways = power<Z>(2, cnts[i]);
                for(int k = 0; k <= need; k++) ways -= choose(cnts[i], k);

                res += power<Z>(2, rem) * mul * ways;

                mul *= choose(cnts[i], need);
            }

            res += mul;

            cout << res << '\n';
        }
    }


    return 0;
}