template<typename T>
std::ostream& operator<<(std::ostream& os, const vector<T> v) {
    for(auto x : v) os << x << " ";
    return os;
}

void solve() {
    
    int n, k; cin >> n >> k;
    vector<int> a(n), b(k);
    for(int &x : a) cin >> x;
    for(int &x : b) cin >> x;
    sort(a.rbegin(), a.rend());
    sort(b.begin(), b.end());

    b.push_back(1e9);
    int cur = 0;
    for(int i = 0; i < b.size(); i++) {
        if(cur + b[i] - 1 < n) {
            cur += b[i] - 1;
            a[cur] = 0;
            cur++;
        } else {
            break;
        }
    }

    // cout << "a : " << a << '\n';

    cout << accumulate(a.begin(), a.end(), 0LL) << '\n';

}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int casi; cin >> casi;
    while(casi-->0) solve();

    return 0;
}