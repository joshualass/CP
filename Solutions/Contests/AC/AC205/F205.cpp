struct FlowEdge {
    int v, u;
    long long cap, flow = 0;
    FlowEdge(int v, int u, long long cap) : v(v), u(u), cap(cap) {}
};
 
struct Dinic {
    const long long flow_inf = 1e18;
    vector<FlowEdge> edges;
    vector<vector<int>> adj;
    int n, m = 0;
    int s, t;
    vector<int> level, ptr;
    queue<int> q;
 
    Dinic(int n, int s, int t) : n(n), s(s), t(t) {
        adj.resize(n);
        level.resize(n);
        ptr.resize(n);
    }
 
    void add_edge(int v, int u, long long cap) {
        edges.emplace_back(v, u, cap);
        edges.emplace_back(u, v, 0);
        adj[v].push_back(m);
        adj[u].push_back(m + 1);
        m += 2;
    }
 
    bool bfs() {
        while (!q.empty()) {
            int v = q.front();
            q.pop();
            for (int id : adj[v]) {
                if (edges[id].cap - edges[id].flow < 1)
                    continue;
                if (level[edges[id].u] != -1)
                    continue;
                level[edges[id].u] = level[v] + 1;
                q.push(edges[id].u);
            }
        }
        return level[t] != -1;
    }
 
    long long dfs(int v, long long pushed) {
        if (pushed == 0)
            return 0;
        if (v == t)
            return pushed;
        for (int& cid = ptr[v]; cid < (int)adj[v].size(); cid++) {
            int id = adj[v][cid];
            int u = edges[id].u;
            if (level[v] + 1 != level[u] || edges[id].cap - edges[id].flow < 1)
                continue;
            long long tr = dfs(u, min(pushed, edges[id].cap - edges[id].flow));
            if (tr == 0)
                continue;
            edges[id].flow += tr;
            edges[id ^ 1].flow -= tr;
            return tr;
        }
        return 0;
    }
 
    long long flow() {
        long long f = 0;
        while (true) {
            fill(level.begin(), level.end(), -1);
            level[s] = 0;
            q.push(s);
            if (!bfs())
                break;
            fill(ptr.begin(), ptr.end(), 0);
            while (long long pushed = dfs(s, flow_inf)) {
                f += pushed;
            }
        }
        return f;
    }
};

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int h, w, n; cin >> h >> w >> n;
    vector<int> row(h), col(w), e1(n), e2(n);
    int p = 0;
    for(int i = 0; i < h; i++) row[i] = p++;
    for(int i = 0; i < w; i++) col[i] = p++;
    for(int i = 0; i < n; i++) e1[i] = p++;
    for(int i = 0; i < n; i++) e2[i] = p++;
    int s = p++;
    int t = p++;
    Dinic dinic(p, s, t);

    for(int i = 0; i < h; i++) dinic.add_edge(s, row[i], 1);
    for(int i = 0; i < w; i++) dinic.add_edge(col[i], t, 1);
    for(int i = 0; i < n; i++) dinic.add_edge(e1[i], e2[i], 1);

    for(int i = 0; i < n; i++) {
        int a, b, c, d; cin >> a >> b >> c >> d;
        for(int j = a - 1; j <= c - 1; j++) {
            dinic.add_edge(row[j], e1[i], 1);
        }
        for(int j = b - 1; j <= d - 1; j++) {
            dinic.add_edge(e2[i], col[j], 1);
        }
    }

    cout << dinic.flow() << '\n';

    return 0;
}
