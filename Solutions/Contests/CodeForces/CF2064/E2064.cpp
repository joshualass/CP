void solve() {
    
    int n; cin >> n;
    vector<int> a(n), c(n);
    for(int i = 0; i < n; i++) {
        int p; cin >> p;
        p--;
        a[p] = i;
    }

    for(int &x : c) cin >> x;

    Z ways = 1;
    Tree<array<int,2>> tree(n);
    Tree_Normal<int> tree_normal(n);
    for(int i = n - 1; i >= 0; i--) {
        int idx = a[i];
        int color = c[idx];
        int l = max(-1, tree.find_last(0, idx, {color, color}).first);
        int r = min(n, tree.find_first(idx + 1, n, {color, color}).first);

        // cout << "i : " << i << " idx : " << idx << " color : " << color << " l : " << l << " r : " << r << '\n';

        ways *= 1 + tree_normal.query(l + 1, r);

        tree.update(idx, {color, color});
        tree_normal.update(idx, 1);

    }

    cout << ways << '\n';

}

